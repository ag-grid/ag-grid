        <p>
            We went on stage. They didn't have
            the strong spot lights this time pointing at the stage, so I could see the room better.
            We used less than 3 minutes, gave our one message, and then boldly told everyone
            that ag-Grid is the best JavaScript data grid in the world. No smoke and mirrors there either.
            I truly believe it is.
        </p>

        <p>
            Every couple of months something in ag-Grid happens that forces me to step back and say "wow".
            Things keep happening above and beyond my expectations for the company. This month, it was
            really a time to realise how far we have come. Had you of told me six months ago we would be
            coming back to Angular Connect as major sponsors, I would not have believed you. It is
            incredible how things have progressed in the space of one year.
            Then in addition to actually sponsoring Angular Connect, to do it so comfortably within the company, both
            financially and to have the support of an ag-Grid team, just enforces how strong the project
            has become and makes me look forward with anticipation to it's future.
        </p>

        <p>
            In hind sight, sponsoring Angular Connect was 100% the right thing to do. We feel it helps
            build our brand as an enterprise company. We spoke to hundreds of delegates,
            a lot of who will end up either becoming customers or helping spread the word about ag-Grid.
            We were also a bit wet behind the ears when it comes to sponsoring conferences, so the best
            way to learn was to just do it. So basically the ag-Grid conference task force got a crash
            course in how it's all done.
        </p>

        <p>
            If you have heard of ag-Grid before, then we hope you are happy with our progress. If you have
            not, then please check us out. I have this feeling we are going to be around for a while.
        </p>

