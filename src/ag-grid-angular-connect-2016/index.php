        <p>
            Part of our sponsorship included presenting a <a hreg="https://www.youtube.com/watch?v=jamr2jmEzh8">
            five minute lightening talk</a> on the main track on the second day.
            It was between two popular talks so we had a lot of people in the room.
            Sean and I were to deliver the talk, with me talking
            and Sean on the laptop driving the demo. We were going to show how ag-Grid can use Angular 2
            internally in the grid for customising the cells, something Sean had been working on for the previous
            weeks. Before getting on stage we stood at the back of the conference hall and I started to
            prepare mentally. Inside I kept saying <i>"this is great, look confident, deliver one message well,
            put energy on the stage, get peoples attention, tell everyone with confidence ag-Grid is
            the best data grid in the world"</i>, complete positive mental thinking and physicing myself
            up. I started bouncing up and down lightly on my heels to get the energy going.
            Our main contact with the Angular Connect organisers was June, a very professional lady
            who was now standing beside us wearing a headset and holding a clipboard looking like she was running a security detail
            for us. One minute to our stage time I couldn't resist, I got her attention and said
            <i>"June, I presume I can't say the word 'f#ck' during the talk"</i>, she released one ear from her
            headset and exclaimed <i>"WHAT?"</i> so I repeated <i>"I presume I can't say the word 'f#ck'?"</i>, <i>"Nooooooooo"</i>
            she said pretty loud with disbelief at the question <i>"what about b#llocks then?"</i>, <i>"Noooooooooooo"</i>
            she repeated as the disbelief went to confusion and possibly panic management <i>"then what about feck
            then, surly I can say the work feck?"</i>, <i>"You can't say any of those words"</i> and worry started to creep
            into her face, <i>"then what's the worst word I can say?". </i> Sean and John were keeling over in their
            failed attempts to constrain their
            laughter, oh dear, the pressure, the poor girl, June I apologise for having to put up with
            me!!! <i>"I'm only joking June, relax, I'm sorry!!!!"</i>, <i>"Please don't do that"</i> . . . . . . . looking
            back, that was a really funny moment!!!
        </p>

        <div style="float: right; padding-left: 20px; padding-bottom: 20px; padding-top: 5px;">
