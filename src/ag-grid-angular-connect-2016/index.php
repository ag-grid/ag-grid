        <p>
            We arrived the day before and set up our stand. We pulled the ag-Grid backdrop out of it's packing
            and saw the 2m x 2m logo against the newly selected background for
            the first time (the background of the logo on our website wouldn't print well according to the print shop,
            it had too much black in it and the ink would bleed). We connected the 20 minute demo movie to
            the large rented TV and saw it play for the first time on something other than our laptops. We cleaned
            down the stand table and stood back. It looked proper. When preparing for
            the conference I had one rule - nothing should look unprofessional - if anything was to look cheap,
            it was to be left out.
            Our stand was in the main hall where food was served and between the two main conference presentation rooms.
            This is the prime location for a stand and given to the top sponsors as you get the traffic of attendees as
            they move between the conference tracks as well as where attendees hang out during breaks.
            Beside our stand was the Progress stand and then after that at the top was Rangle.io. To the other
            side was Angular Boot Camp. These were all well known brands within the JavaScript community so it
            felt pretty cool to be hitting at the same weight. We left and I mentally prepared myself for hardly
            anyone coming to our stand, just in case, that way I can only be positively surprised if anything
            better happened - I am not sure if preparing for the worst is a good thing to do, it's the
            opposite of positive thinking. However sometimes I do it.
        </p>

        <p>
            On the first day of the conference John and I walked up to the stand at 8:05am with our takeaway coffees.
            There were already people
            on the conference floor. I started to play our 20 minute looping video and then as I turned
            around, someone came straight up and engaged. John also got nabbed within seconds.
            Between the first person talking to me and the next I had literally 10 seconds of a breather
            and looked around and saw that Sean had arrived and both Sean and John were busy talking
            to more interested delegates. Then I was engaged again. We were all full time busy right from the start
            up until the main conference track started at 9:30am. My coffee was cold. Wow. That
            90 minutes ripped the fear of looking silly right out of me. In the quiet few moments that followed,
            I looked at John and Sean and said <i>"We're gonna need a bigger boat"</i>.
            It felt like I was taken out of normal life and placed on a big stand of a big company and told to act like
            I belonged there. It defiantly felt that strange, but there were no smoke and mirrors here.
        </p>

        <p>
            The term 'smoke and mirrors' is a term used in start-ups where you pretend you are bigger
            than you actually are in order to get business. ag-Grid needed none of this, the story spoke
            for itself. The company was completely self funded from sales with no investment
            capital, including no capital from me apart from sweat equity. Typically a tech
            start-up raises funds via investment, creates a product, and then has a target of becoming profitable before
            the funds run out. ag-Grid has not raised capital and was creating profit.
            The product offering is so successful that the company skipped the start-up stage with regards
            financial bootstrapping. So the risk associated with dealing with a young startup doesn't
            exist for us because of the zero leverage existing within the company.
            All of the people working in ag-Grid are on full salaries.
            The people at the stand were not my friends and family - another trick used by some start-ups!
            With regards affordability of the conference for us, the cost of the conference was easily
            covered by the monthly ag-Grid sales. That month we still turned profit similar to the months
            before. So there was never any doubt in my mind that we belonged sponsoring Angular Connect
            at that level, the only discomfort was due to fear of the unknown and us as a team not keeping
            up emotionally with how successful ag-Grid was becoming.
        </p>

        <div style="float: right; padding-left: 20px; padding-bottom: 20px; padding-top: 5px;">
            <img src="../images/angularConnect2016/agGridStand.png"/>
