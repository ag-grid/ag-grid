        <p>
            When I spoke at Angular Connect in 2015 ag-Grid had 2,000 downloads per month with no backing
            company (just me in my spare time). For Angular Connect 2016 ag-Grid had 32,000 downloads per month
            with offices, employees, an enterprise offering and hundreds of customers. That story
            speaks for itself.
        </p>

        <p>
            The rest of the conference continued with the same energy as the first 90 minutes. During the
            talking tracks the stand was quiet as delegates attended the talks. We used this quiet time
            for more involved one-on-one sessions with people who wanted to get a deeper knowledge of ag-Grid
            and for them to show us how they wanted to use ag-Grid.
            As for the time between tracks, our stand was very busy. It was constant talking to interested
            parties, showing demo's on our laptops, answering questions, getting feedback.
        </p>

        <p>
            At one point between tracks, one individual came up and said <i>"I wanted to say thank you for
            making my life so much easier".</i> We had a conversation for a few minutes and he asked if he could
            take a selfie with me! Now I know fully well that ag-Grid
            is used by companies, a lot of which are banks and private equity funds, and solving the worlds
            humanitarian problems is not what we do. But for this guy, his problem was displaying a large
            amount of data on a web page and ag-Grid helped him do this.
            He wanted a selfie. That was cool.
        </p>

        <div style="float: left; padding-right: 20px; padding-bottom: 20px; padding-top: 5px;">
            <img src="../images/angularConnect2016/viewFromStage.png"/>
            <div style="padding: 0px; width: 400px; color: darkgrey; font-size: 12px;">
